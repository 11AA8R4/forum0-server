function slugify(text) {
  const from = "ãàáäâẽèéëêìíïîõòóöôùúüûñç·";
  const to =   "aaaaaeeeeeiiiiooooouuuunc-";
  const blackListvocals = 
  / a | e | i | o | u /g;
  const blackListTerms = 
  / em | na | no | da | de | do | com | sem | como | mais | menos | meu | minha | que | qual /g;

  //let text = search;
  for (var i = 0, len = from.length; i < len; i++)
    text = text.replace(new RegExp(from.charAt(i), 'g'), to.charAt(i));

  return text
    .toString()                     // Cast to string
    .replace(blackListvocals,' ')     // Remove black list vocals
    .toLowerCase()                  // Convert the string to lowercase letters
    .trim()                         // Remove whitespace from both sides of a string
    .replace(/[\.,-\/#!$%\^&\*;:{}=\-_`~()@\+\?><\[\]\+]/g, '')
    .replace(blackListTerms,'-')     // Remove black list terms
    .replace(/\s\s+/g, ' ')         // Multimple spaces with one
};

